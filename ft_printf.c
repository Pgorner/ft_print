#include "ft_printf.h"

int	ft_printf(const char *str, ...)
{
	va_list	args;
	int		i;
	int		len;

	i = 0;
	len = 0;
	va_start(args, str);
	while (str[i])
	{
		if (str[i] == '%')
		{
			len += ft_flag(args, str[i + 1]);
			++i;
		}
		else
			len += ft_putchar(str[i]);
		++i;
	}
	va_end(args);
	return (len);
}
